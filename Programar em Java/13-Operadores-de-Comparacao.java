package com.caffeinealgorithm.programaremjava;

public class OperadoresDeComparacao {
    public void Run() {
        int x = 10;
        int y = 20;

        if (x <= y || x == y)
            System.out.println("Esta condição é verdadeira!");
        else
            System.out.println("Esta condição é falsa!");
    }
}